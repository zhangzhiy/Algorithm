/**
 * Definition for singly-linked list.
 * public class ListNode {
 *     int val;
 *     ListNode next;
 *     ListNode() {}
 *     ListNode(int val) { this.val = val; }
 *     ListNode(int val, ListNode next) { this.val = val; this.next = next; }
 * }
 */
class Solution {
    public ListNode mergeTwoLists(ListNode l1, ListNode l2) {
        ListNode head=l1;
        if(l1.val>l2.val){
            head=l2;
        }
        ListNode temp=head;
        while (temp!=null){
            if(l1.val>l2.val){
                temp.next=l2;
               temp=l2.next;
            }else {
                temp.next=l1;
                temp=l1.next;
            }
        }
        return head;
    }
}
