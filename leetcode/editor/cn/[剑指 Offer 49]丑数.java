class Solution {
    public int nthUglyNumber(int n) {
        if(n==1){
            return 1;
        }
        int index=1;
        int num=1;
        while (index<=n){
            num++;
            if(num%2==0 || num%3 ==0 || num%5==0){
                index++;
                if(index==n){
                    return num;
                }
            }

        }
        return num;
    }
}
